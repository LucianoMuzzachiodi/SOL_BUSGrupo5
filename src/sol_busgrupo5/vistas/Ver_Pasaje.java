    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        try{
            if(Eliminar.isEnabled()){
                int aux = pasajeData.eliminarPasaje(Integer.parseInt(ID_Pasaje.getText()));
                if(aux==1){
                JOptionPane.showMessageDialog(this, "Pasaje removido.");
                Listar_Pasaje();
                Modificar.setEnabled(false);
                Eliminar.setEnabled(false);
                } else if (aux==0){
                JOptionPane.showMessageDialog(this, "no se pudo remover.");
                Modificar.setEnabled(false);
                Eliminar.setEnabled(false);
                Listar_Pasaje();
                }
            }
        }catch(NumberFormatException NFE){
            JOptionPane.showMessageDialog(this, "Valores erroneos.");
            ID_Pasajero.requestFocus();
            ID_Colectivo.requestFocus();
            ID_Ruta.requestFocus();
        }
    }//GEN-LAST:event_EliminarActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        if(!ID_Pasaje.getText().isEmpty() && pasajeData.BuscarPasaje(Integer.parseInt(ID_Pasaje.getText()))!=null){
            System.out.println(pasajeData.BuscarPasaje(Integer.parseInt(ID_Pasaje.getText())));
            Pasaje pasaje = pasajeData.BuscarPasaje(Integer.parseInt(ID_Pasaje.getText()));
            Listar_Pasaje(pasaje);
            Eliminar.setEnabled(true);
            Modificar.setEnabled(true);
        } else{
            Listar_Pasaje();
        }
        
    }//GEN-LAST:event_BuscarActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        try{
            if(Modificar.isEnabled()){
                Pasaje pasaje = new Pasaje(Integer.parseInt(ID_Pasaje.getText()),pasajeroData.buscar("ID", Integer.parseInt(ID_Pasajero.getText())),colectivoData.buscar(Integer.parseInt(ID_Colectivo.getText())),rutaData.buscarPorID(Integer.parseInt(ID_Ruta.getText())),Date.valueOf(LocalDate.parse(Fecha_Viaje.getText())),Time.valueOf(Hora_Viaje.getText()),Integer.parseInt(Asiento.getText()),Double.parseDouble(Precio.getText()),Boolean.parseBoolean(Estado.getText()),Date.valueOf(Fecha_Venta.getText()));
                int aux = pasajeData.modificar(pasaje);
                if(aux==1){
                    JOptionPane.showMessageDialog(this, "Se ha modificado el Pasaje.");
                    Listar_Pasaje();
                    Modificar.setEnabled(false);
                    Eliminar.setEnabled(false);
                } else if (aux==0){
                    JOptionPane.showMessageDialog(this, "No se pudo modificar.");
                    Listar_Pasaje();
                    Modificar.setEnabled(false);
                    Eliminar.setEnabled(false);
                }
            }
        }catch (NumberFormatException NFE){
            JOptionPane.showMessageDialog(this, "Valores erroneos.");
        }
    }//GEN-LAST:event_ModificarActionPerformed

